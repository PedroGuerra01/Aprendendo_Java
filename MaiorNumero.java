public class MaiorNumero {
    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = 10;

        if (numero1 > numero2) {
            System.out.println("O número 1 é maior.");
        } else if (numero2 > numero1) {
            System.out.println("O número 2 é maior.");
        } else {
            System.out.println("Os números são iguais.");
        }
    }
}
